    /**
     * 
     * @param evt
     */
    private void botonDarDeBajaActionPerformed(java.awt.event.ActionEvent evt) {                                         
    	
    	DefaultTableModel tm = (DefaultTableModel) this.tablaUsuarios.getModel();
		int j[] = this.tablaUsuarios.getSelectedRows();
    	
    	if(j.length == 0) {
			JOptionPane.showMessageDialog(this,
					"Tienes que seleccionar algun usuario !",
					"Error",
					JOptionPane.ERROR_MESSAGE);
		}
    	else {
    	Object[] options1 = { "Yes", "No"};
    	int n = JOptionPane.showOptionDialog(this, "¿ Estas seguro que quieres dar de baja a los usuarios seleccionados ?", "Dar de baja", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
    			null,
    			null,
    			null);
    	
    	if (n == 0) {
    	
	    	for (int i = 0; i < j.length; i++) { 
       			String nick = String.valueOf(tm.getValueAt(j[i],2));
       			String tipo = String.valueOf(tm.getValueAt(j[i],5));
       			if(tipo.equals(this.usuarioActual.getTipo()))
       				JOptionPane.showMessageDialog(this,
       						"No puedes darte de baja así mismo desde esta opción",
    			    		"Error",
    			    		JOptionPane.ERROR_MESSAGE);
       			else {
       				try {
       					this.control.darDeBajaA(nick);
       				} catch (IDUsuarioNoExiste e) {
						e.printStackTrace();
					}
       			}
       			
       		}
       		JOptionPane.showMessageDialog(this,
					"Se han dado de baja correctamente",
					"Éxito",
					JOptionPane.INFORMATION_MESSAGE);
       		this.actualizarTablaUsuarios();
    	}
    	}
       
    }                                        

    /**
     * 
     * @param evt
     */
    private void botonFiltrarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String nombre = this.nombreTexto.getText();
        String apellidos = this.apellidosTexto.getText();
        String correo = this.correoTexto.getText();
        
        List<Usuario> usuarios;
        
        Usuario u = new Usuario(nombre, apellidos, correo);
        try {
    		usuarios = this.control.filtrarUsu(u);
    		Iterator<Usuario> itr = usuarios.iterator();
    		DefaultTableModel model = (DefaultTableModel) this.tablaUsuarios.getModel();
    		int numColumn = model.getRowCount();
    		for(int i = numColumn - 1; i >= 0; i--){
    			model.removeRow(i);
    		}
    		while(itr.hasNext()) {
    			Usuario p = itr.next();
    			//A�ADIR A LISTA PRINCIPAL
    			model.addRow(new Object[]{p.getNombre(), p.getApellidos(), p.getNick(), p.getCorreo(), p.getTelefono(), p.getTipo()});
    		}
    	} catch (SQLException e) {
    		
    		e.printStackTrace();
    	}
    }                                            

