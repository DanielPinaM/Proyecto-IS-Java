    /**
     * 
     */
 private void actualizarTablaUsuarios() {
    	
    	List<Usuario> usuarios = new ArrayList<Usuario>();
		try {
			usuarios = this.control.consultarUsuarios();
			Iterator<Usuario> itr = usuarios.iterator();
			DefaultTableModel model = (DefaultTableModel) this.tablaUsuarios.getModel();
			int numColumn = model.getRowCount();
			for(int i = numColumn - 1; i >= 0; i--){
				model.removeRow(i);
			}
			while(itr.hasNext()) {
				Usuario u = itr.next();
				//A�ADIR A LISTA PRINCIPAL
				model.addRow(new Object[]{u.getNombre(), u.getApellidos(), u.getNick(), u.getCorreo(), u.getTelefono(), u.getTipo()});
			}
		} catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
		}
    }

